# String slicing and Operation on Strings

me = "Nishu,Nilesh"

print(len(me)) # It is used to find the length of the string.

print(me[0:5]) # At the fifth place the qoma in the variable me , is not gonna Print note it. 
print(me[:]) # It is automatically gonna Print the full length of the variable me.
print(me[:-7]) # the negative means the full lenght(in Number) of the string - (negative no. we wrote).
print(me[-8:-2])

